// Switch Staatment 


let day = 2;

switch (day){
    case 0:
        console.log("day 1");
        break;
    case 1:
        console.log("day 2");
        break;
    case 2:
        console.log("day 3");
        break;
    case 3:
        console.log("day 4");
        break;
    case 4:
        console.log("day 5");
        break;
    case 5:
        console.log("day 6");
        break;
    case 6:
        console.log("day 7");
        break;
    default:
        console.log("invalid day")
}
